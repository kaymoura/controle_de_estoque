    private void btnAddMoviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddMoviActionPerformed
        
        //Primeiramente recuperar os dados preenchidos
        
        Movimento mAux = new Movimento();
        mAux.setDataMovto(jDateChooser1.getDate());
        mAux.setQdtMovto(Double.parseDouble(txtQtd.getText()));
        
        produto.addMovto(mAux);

        DefaultTableModel tabelaProduto = (DefaultTableModel) tblMovi.getModel();
        //tabelaProduto.addRow(new Object[]{theDate});
        Object[] novaMovimentacao = new Object[]{
            produto.getId(), //pos1 do array
            mAux.getQdtMovto(), //pos2 do array
            mAux.getDataMovto()
        };

        //adiciona o novo movimento na tabela
        tabelaProduto.addRow(novaMovimentacao);

        //limparCampos
        txtCodigo.setText("");
        txtQtd.setText("");
        
        txtCodigo.requestFocus();
        
       /* System.out.println("Descricao: " + produto.getDescricao());
        System.out.println("Qtd      : " + produto.getQtd());*/
    }//GEN-LAST:event_btnAddMoviActionPerformed

