    private void tblMoviMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMoviMouseClicked
        // TODO add your handling code here:
        try{
            DefaultTableModel model = (DefaultTableModel)tblMovi.getModel();
            int seectedRow = tblMovi.getSelectedRow();
            Date date = new SimpleDateFormat("dd/MM/yyyy").parse((String)model.getValueAt(seectedRow, 0).toString());
            jDateChooser1.setDate(date);
        } catch (ParseException ex){
            Logger.getLogger(PageMovimentos.class.getName()).log(Level.SEVERE,null,ex);
        } 
    }//GEN-LAST:event_tblMoviMouseClicked
    
